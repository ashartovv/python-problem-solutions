# Problem:
# Declare the following list containing student grades:
#
# m = [2, 3, 5, 5, 2, 2, 3, 3, 4, 5, 4, 4]
#
# Using list slicing, select the elements from the 3rd to the 7th
# position (inclusive) and display them in reverse order.
#
# Note:
# The element number starts from 1 (normal counting),
# while the index starts from 0.
#
# Input:
# No input.
#
# Output:
# The selected elements in reverse order as a list.
#
# Example:
# Output:
# [3, 2, 5, 5, 2]

m = [2, 3, 5, 5, 2, 2, 3, 3, 4, 5, 4, 4]

print(m[6:1:-1])